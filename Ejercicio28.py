print("Vamos a ver si el primero número es mayor, menor o igual que el segundo:")
num1=int(input("Escriba el primer número, por favor:"))
num2=int(input("Ahora escriba el segundo número, por favor:"))
if num1 > num2:
    print("El primer número es mayor que el segundo")
elif num1 < num2:
    print("El primer número es menor que el segundo")
else:
    print("El primer número es igual que el segundo")